    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        String discount = jTextField1.getText();
        String text = discount + evt.getKeyChar();

        if (!Pattern.compile("[1-9][0-9]*").matcher(text).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField1KeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int r = hr.jTable1.getSelectedRow();

        if (hr.discount.get(r).equals(0)) {
            hr.discount.set(r, jTextField1.getText());
        } else {
            int option = JOptionPane.showConfirmDialog(this, "This product already have a discount.Do you want to update?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

            if (option == JOptionPane.YES_OPTION) {

                hr.discount.set(r, jTextField1.getText());
            } else {
                this.dispose();
            }
        }
        this.dispose();

        Item_stock_selector iss = new Item_stock_selector(hr, true);
        iss.totalcal();
    }//GEN-LAST:event_jButton1ActionPerformed

